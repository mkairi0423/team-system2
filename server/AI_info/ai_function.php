<?php

// AI関連の関数をまとめるファイル
// ここでは、AIに関連する共通の関数やルールを定義します。
// 例えば、食材名の表記揺れを防ぐための変換関数などをここに置いておくと便利です。
function henkan($input_name)
{
    // 1. 前後の余分な空白（全角・半角）を削除
    $name = trim(mb_convert_kana($input_name, "s"));

    // 2. カタカナを「ひらがな」に変換、全角英数字を「半角」に変換
    // 「c」: 全角カタカナをひらがなに変換
    // 「V」: 濁点付きの文字を1文字に統合
    // 「a」: 全角英数字を半角に変換
    $name = mb_convert_kana($name, "cVa", "UTF-8");

    // 3. アルファベットがあった場合、大文字を「小文字」に統一（例：牛肉A5 → 牛肉a5）
    $name = mb_strtolower($name); //ここ

    // 4. 【おまけ】「じゃが芋」などの漢字混じりをどうするか？
    // 完全にひらがな化したい場合は、のちのち画面5・6で作る「Gemini API」に
    // 「この食材名をひらがな5文字以内に変換して」と頼むのが一番賢いです。
    // ここでは、まずカタカナ・全角半角のバラつきを完全に破壊して統一します。

    return $name;
}



?>